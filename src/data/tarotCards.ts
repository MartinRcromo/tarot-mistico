export const getCardById = (id: number): TarotCard | undefined => {
  return tarotCards.find(card => card.id === id);
};

export const getRandomCard = (): TarotCard => {
  const randomIndex = Math.floor(Math.random() * tarotCards.length);
  return tarotCards[randomIndex];
};

export const getCardsByArcana = (arcana: 'major' | 'minor'): TarotCard[] => {
  return tarotCards.filter(card => card.arcana === arcana);
};

export const getCardsBySuit = (suit: string): TarotCard[] => {
  return tarotCards.filter(card => card.suit === suit);
};
